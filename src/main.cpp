Map GetTestMap() {
  Map map("assets/lv1-tileset.png");
  map.LoadMap("assets/lv1-tileset.png", level_data);

  return map;
}

int main() {
  sf::RenderWindow window(sf::VideoMode(1200, 800), "Super Mario");


  sf::Vector2u window_size = window.getSize();
  float scale_x = static_cast<float>(window_size.x) / (TILES_X * TILE_SIZE);
  float scale_y = static_cast<float>(window_size.y) / (TILES_Y * TILE_SIZE);
  sf::View view(sf::FloatRect(0, 0, window_size.x, window_size.y));
  view.setSize(scale_x * TILES_X * TILE_SIZE, scale_y * TILES_Y * TILE_SIZE);

  float zoom_factor = 1.0f;
  view.zoom(zoom_factor);

  window.setView(view);

  Player mario("assets/MarioIdle.png");

  Map map = GetTestMap();

  // Main game loop
  while (window.isOpen()) {
    sf::Event event;
    while (window.pollEvent(event)) {
      if (event.type == sf::Event::Closed) {
        window.close();
      }
    }

    mario.HandleInput();
    mario.Update(map.GetCollisionBounds());

    window.clear();
    map.Draw(window);
    mario.Draw(window);

    window.display();
  }

  return EXIT_SUCCESS;
}
